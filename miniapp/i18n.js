/**
 * Get translation for a key
 * @param {string} key - Translation key
 * @param {object} params - Optional parameters for interpolation
 * @returns {string} Translated text
 */
function t(key, params = {}) {
    const translations = TRANSLATIONS[currentLanguage] || TRANSLATIONS['uz'];
    let text = translations[key] || TRANSLATIONS['uz'][key] || key;

    // Simple parameter interpolation
    Object.keys(params).forEach(param => {
        text = text.replace(`{${param}}`, params[param]);
    });

    return text;
}

/**
 * Set current language
 * @param {string} langCode - Language code
 */
function setLanguage(langCode) {
    if (TRANSLATIONS[langCode]) {
        currentLanguage = langCode;
        localStorage.setItem('caravan_lang', langCode);
        updateUITexts();
        return true;
    }
    return false;
}

/**
 * Get current language code
 * @returns {string} Current language code
 */
function getLanguage() {
    return currentLanguage;
}

/**
 * Get language info
 * @param {string} langCode - Language code
 * @returns {object} Language info
 */
function getLanguageInfo(langCode) {
    return LANGUAGE_CODES[langCode] || LANGUAGE_CODES['uz'];
}

/**
 * Get all available languages
 * @returns {array} Array of language objects
 */
function getAvailableLanguages() {
    return Object.keys(LANGUAGE_CODES).map(code => ({
        code,
        ...LANGUAGE_CODES[code]
    }));
}

/**
 * Update all UI texts with current language
 */
function updateUITexts() {
    // Update elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = t(key);
    });

    // Update elements with data-i18n-placeholder attribute
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = t(key);
    });

    // Update specific elements by ID
    const elementsToUpdate = {
        // Home screen
        'walletLabel': 'my_coins',
        'servicesTitle': 'services',
        'recentAppsTitle': 'recent_apps',

        // Navigation
        'navHomeLabel': 'nav_home',
        'navMapLabel': 'nav_map',
        'navHistoryLabel': 'nav_history',
        'navProfileLabel': 'nav_profile'
    };

    Object.entries(elementsToUpdate).forEach(([id, key]) => {
        const el = document.getElementById(id);
        if (el) el.textContent = t(key);
    });

    // Dispatch event for custom handlers
    document.dispatchEvent(new CustomEvent('languageChanged', {
        detail: { language: currentLanguage }
    }));
}

/**
 * Initialize i18n on page load
 */
function initI18n() {
    // Get language from Telegram WebApp if available
    const tg = window.Telegram?.WebApp;
    if (tg?.initDataUnsafe?.user?.language_code) {
        const tgLang = tg.initDataUnsafe.user.language_code;
        // Map Telegram language codes to our codes
        const langMap = {
            'uz': 'uz',
            'ru': 'ru',
            'en': 'en',
            'zh': 'zh',
            'tr': 'tr',
            'kk': 'kk',
            'ky': 'ky',
            'tg': 'tj',
            'tk': 'tk'
        };
        const mappedLang = langMap[tgLang];
        if (mappedLang && !localStorage.getItem('caravan_lang')) {
            currentLanguage = mappedLang;
            localStorage.setItem('caravan_lang', mappedLang);
        }
    }

    updateUITexts();
}

